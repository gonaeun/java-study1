package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 20; // 변수 초기화  10->20
        // 자바에서 '=' 은 '오른쪽의 값을 왼쪽에 저장한다'는 의미

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
        // 그릇에 a라는 값을 담고, 그 값을 읽기
        // a의 값만 변경하면 출력값을 편리하게 변경할 수 있음
    }
}
