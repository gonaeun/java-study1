package loop;

public class While1_1 {
    public static void main(String[] args) {
        int count = 0;

        count = count +1;
        System.out.println("현재 숫자는 : "+count);
        count = count +1;
        System.out.println("현재 숫자는 : "+count);
        count = count +1;
        System.out.println("현재 숫자는 : "+count);
        // 단순히 count의 값을 1씩 3번 더하는 예제
        // 특정 코드를 반복하는 "반복문"으로 간단히 작성해보자
    }
}
